function getNextBus(line, type, stop) {
  const now = new Date();

  const current =
    now.getHours() * 60 + now.getMinutes();

  const times = busData[line][type][stop];

  if (!times) {
    return "データなし";
  }

  for (const time of times) {
    const [h, m] = time.split(":").map(Number);

    const busTime = h * 60 + m;

    if (busTime >= current) {
      return time;
    }
  }

  return "本日の運行終了";
                   }
let currentLine = "higashisakai";
let currentDay = "weekday";
let currentDirection = "up";

function renderBusTimes() {

  const key =
    currentDay + "_" + currentDirection;

  const data =
    busData[currentLine][key];

  const container =
    document.getElementById("bus-times");

  container.innerHTML = "";

  for (const stop in data) {

    const times = data[stop];

    const div = document.createElement("div");

    div.className = "stop-card";

    div.innerHTML = `
      <h3>${stop}</h3>
      <p>${times.join(" ・ ")}</p>
    `;

    container.appendChild(div);
  }
}

window.onload = function () {
  renderBusTimes();
};
async function fetchDelayInfo() {

  const delayText =
    document.getElementById("delay-text");

  try {

    const url =
  "https://api.allorigins.win/raw?url=" +
  encodeURIComponent(
    "https://buscatch.jp/rt3/unko_rosen_list.php?id=kariya-city"
  );

    const response = await fetch(url);

    const html = await response.text();

    const parser = new DOMParser();

    const doc =
      parser.parseFromString(html, "text/html");

    const text =
  doc.documentElement.textContent || "";

    const results = [];

    const matches =
  text.match(/[^。\n]*遅れ[^。\n]*/g);

    if (matches) {
      results.push(...matches);
    }

    if (results.length === 0) {
      results.push("現在遅延情報はありません");
    }

    delayText.innerHTML =
      results.join("<br>");
    document.getElementById("delayContainer").style.display = "block";

  } catch (error) {

    delayText.textContent =
      "遅延情報を取得できません";

  }
}

fetchDelayInfo();

setInterval(fetchDelayInfo, 30000);
